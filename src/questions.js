// Считает результат теста по массиву баллов (по одному числу на каждый вопрос)
function calculateResult(testId, points) {
  const test = tests[testId];
  const total = points.reduce((sum, p) => sum + p, 0);

  const tier = test.resultTiers.find(t => total >= t.min && total <= t.max);
  const result = tier ? tier.key : test.resultTiers[test.resultTiers.length - 1].key;

  return { total, result };
}

