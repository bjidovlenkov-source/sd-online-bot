// Считает результат теста по массиву баллов (по одному числу на каждый вопрос)
function calculateResult(testId, points) {
  const test = tests[testId];
  const total = points.reduce((sum, p) => sum + p, 0);
  const hasCriticalFlag = (test.criticalIndices || []).some(i => points[i] === 0);

  let result;
  if (total >= test.thresholds.green) {
    result = 'green';
  } else if (total >= test.thresholds.yellow) {
    result = 'yellow';
  } else {
    result = 'red';
  }

  // Критический флаг не даёт результату быть зелёным
  if (hasCriticalFlag && result === 'green') {
    result = 'yellow';
  }

  return { total, result };
}

